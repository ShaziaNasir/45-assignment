function make_album(artistName, albumTitle) {
    var musicAlbum = {
        artist: artistName,
        album: albumTitle
    };
    return musicAlbum;
}
console.log(make_album("Hamza", "trial"));
console.log(make_album("Ali", "new"));
console.log(make_album("Rafay", "Tera bin"));
console.log(make_album, (""));
