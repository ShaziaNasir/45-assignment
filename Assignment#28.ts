let personAge = 45;
if (personAge === 2) {
    console.log("He is a baby");
}
if (personAge == 2 && personAge <= 4) {
    console.log("He is toddler");
}
if (personAge > 4 && personAge <= 13) {
    console.log("He is a kid");
}
if (personAge > 20 && personAge <= 65) {
    console.log("He is adult");
}
if (personAge >= 65) {
    console.log("He is an elder");
}